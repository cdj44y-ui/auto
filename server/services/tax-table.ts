/**
 * 간이세액표 조회
 * @param monthlyWage 월 급여 (원)
 * @param dependents 부양가족 수 (본인 포함, 최소 1)
 * @returns 소득세 (원)
 */
export function lookupSimplifiedTax(monthlyWage: number, dependents: number): number {
  // 부양가족 수 보정 (1~7 범위)
  const depIdx = Math.max(0, Math.min(6, (dependents || 1) - 1));

  // 100만원 미만: 소득세 없음
  if (monthlyWage < 1_000_000) return 0;

  // 1000만원 초과: 최고 구간 + 초과분 38% 적용 (소득세법 제55조 제1항)
  if (monthlyWage >= 10_000_000) {
    const lastRow = TAX_TABLE[TAX_TABLE.length - 1];
    const baseTax = lastRow[2 + depIdx];
    const excess = monthlyWage - 10_000_000;
    return Math.floor(baseTax + excess * 0.38);
  }

  for (const row of TAX_TABLE) {
    if (monthlyWage >= row[0] && monthlyWage < row[1]) {
      return row[2 + depIdx];
    }
  }

  return 0;
}
